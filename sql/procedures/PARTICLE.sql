
DROP PROCEDURE IF EXISTS PARTICLE;
DELIMITER |
CREATE PROCEDURE PARTICLE (IN VMOT VARCHAR(50), IN VRACINE VARCHAR(50), OUT VID_RACINE INT)   


BEGIN
DECLARE VID_MOT INT DEFAULT 0;

     INSERT INTO MOT VALUES (NULL,VMOT);
     
     SELECT ID_MOT INTO VID_MOT
     FROM MOT
     WHERE MOT=VMOT;

     INSERT INTO MOT_RACINE VALUES (NULL,VRACINE,VID_MOT);  

     SELECT ID_RACINE INTO VID_RACINE
     FROM MOT_RACINE
     WHERE MOT=VRACINE;

END|         

DELIMITER ;
CALL PARTICLE("VBA", "PYTHON", @ID_RACINE);
CALL PARTICLE("PHP", "JAVA", @ID_RACINE);
SELECT @ID_RACINE;
